    for n=1:length(t)
       z(n,k)=prod(Q1(n,2:k))/F1(k)*d(N0-...
       p,k+2)+prod(Q2(n,2:k))/F1(k)*d(N0-2,k+3);
    end  
p=p+1;
end
z(:,1)=y0+q.*d(N0,2)+q.*(q-1)/2.*d(N0-1,3);
z=sum(z');
