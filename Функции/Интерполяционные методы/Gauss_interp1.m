   Q1(:,m)=q.*(q.^2-(m-1)^2);
   Q2(:,m)=Q1(:,m).*(q'-m);
end
for m=2:NN
   F1(m)=factorial(2*m-1);
   F2(m)=factorial(2*m);
end
p=1;
